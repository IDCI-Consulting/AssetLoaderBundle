namespace IDCI\Bundle\AssetLoaderBundle\DependencyInjection\Compiler;

use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Compiler\CompilerPassInterface;

class AssetLoaderCompilerPass implements CompilerPassInterface
{
    /**
     * {@inheritdoc}
     */
    public function process(ContainerBuilder $container)
    {
        if (!$container->hasDefinition('idci_asset_loader.asset_loader')) {
            return;
        }

        $assetLoader = $container->getDefinition('idci_asset_loader.asset_loader');
        $assetLoader->addMethodCall('loadAll');
    }
}
